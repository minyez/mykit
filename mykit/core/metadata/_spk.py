def cond_any(a, b, c):
    '''no specific condition
    '''
    return 0


def cond_a_lt_b(a, b, c):
    '''check if a < b
    '''
    if a < b:
        return 1
    if a > b:
        return 0
    _raise_need_standardize()


def cond_c_lt_a(a, b, c):
    '''check if c/a < 1
    '''
    if c < a:
        return 1
    if c > a:
        return 0
    _raise_need_standardize()


def cond_curt_a_lt_sqrt_c(a, b, c):
    '''check if cubic root of a < square root of c
    '''
    _r = power(a, 1.0/3.0) / power(c, 0.5)
    if _r < 1:
        return 1
    if _r > 1:
        return 0
    _raise_need_standardize()


def cond_abc_invsq(a, b, c):
    '''Check relation of {a, b, c}^-2
    '''
    invsq = power([a,b,c], -2)
    _sum = sum(invsq)
    k0 = 2.0 * invsq[0] - _sum > 0.0
    k1 = 2.0 * invsq[2] - _sum > 0.0
    k2 = all(2.0 * invsq - _sum < 0.0)
    for i, v in enumerate([k0, k1, k2]):
        if v:
            return i
    _raise_need_standardize()


def cond_max_bc_noneq_left(a, b, c):
    '''Check whether b or c is the largest value, and the other two unequal
    '''
    if b > max(a, c) and a != c:
        return 0
    if c > max(a, b) and b != a:
        return 1
    _raise_need_standardize()


